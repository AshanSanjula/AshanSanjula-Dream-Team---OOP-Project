    private void ClearReservationsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ClearReservationsButtonActionPerformed
        // TODO add your handling code here:
         int row = ReservationTable.getSelectedRow();
        String cell = ReservationTable.getModel().getValueAt(row,0).toString();
        String sql = "DELETE FROM reserve WHERE RID = "+ cell;
        try{
         
            pst = con.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Deleted Successfully");
             ReservationLoad();
        }catch(Exception e)
        {
        JOptionPane.showMessageDialog(null, e);
        }
    
    }//GEN-LAST:event_ClearReservationsButtonActionPerformed

