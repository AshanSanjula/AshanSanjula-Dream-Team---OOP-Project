    public void ReservationLoad()
    {
        try {
            pst = con.prepareStatement("select * from reserve");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = (ResultSetMetaData) rs.getMetaData();
            int c;
            c = rsd.getColumnCount();
            
            DefaultTableModel d =(DefaultTableModel)ReservationTable.getModel();
            
            d.setRowCount(0);
            
            while(rs.next())
            {
            Vector v = new Vector();
            
            for(int i=0; i<=c; i++)
            {
            v.add(rs.getString("RID"));    
            v.add(rs.getString("Rname"));
             v.add(rs.getString("RNID"));
             v.add(rs.getString("DOW"));
              v.add(rs.getString("Sets"));
               v.add(rs.getString("RTotal"));
            }
            d.addRow(v);
            }
            
            
            
                    } catch (SQLException ex) {
            Logger.getLogger(Reservations.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
